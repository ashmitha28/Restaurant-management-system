    private void btnAddItemsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemsActionPerformed

        DefaultTableModel m = (DefaultTableModel) tblBill.getModel();

        String name; 
        double rate, quantity, amount;
        
        name = cmbItems.getSelectedItem().toString();
        
        String q = "select * from item where item_name = '"+name+"'";
        
        try
        {
            rs = stmt.executeQuery(q);
            rs.first();

            rate = Double.parseDouble(rs.getString("rates"));
            quantity = Double.parseDouble(spnQty.getValue().toString());
            amount = rate * quantity;
            
            m.addRow(new Object[]{name, rate, quantity, amount});
            
            
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        updateGrandTotal();
        
        
        
        


    }//GEN-LAST:event_btnAddItemsActionPerformed

