    {
         try {

            String q = "call DisplayCategory()";
            rs2 = stmt2.executeQuery(q);

            while(rs2.next())
            {   
                cmbCategory.addItem(rs2.getString("category_name"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }
    
    private void updateGrandTotal()
    {
        DefaultTableModel m = (DefaultTableModel) tblBill.getModel();
        
        Double total = 0.0;
        
        for (int i = 0; i<tblBill.getRowCount(); i++)
        {
            total += Double.parseDouble(tblBill.getValueAt(i, 3).toString());
        }
        
        txtGrandTotal.setText(total+"");
    }
    
    private int orderIdGenerator()
    {
        Random random = new Random();
        return 10000 + random.nextInt(90000);
    }
