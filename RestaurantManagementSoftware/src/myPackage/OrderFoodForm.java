    private void btnOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderActionPerformed

        try {
            
            int row, column = 0;
           
            String payment = cmbPaymentMethod.getSelectedItem().toString();
            

            for (row = 0; row < tblBill.getRowCount(); row++) {
                column = 0;
                String name = tblBill.getValueAt(row, column).toString();
                column++;
                column++;
                String quantity = tblBill.getValueAt(row, column).toString();
                String q = "call updateorderitem("+this.orderId+", '"+name+"', "+quantity+", '"+payment+"', '"+this.userS+"')";
                stmt.executeUpdate(q);
            }
            
            JOptionPane.showMessageDialog(null, "Order made successfully!");
            DefaultTableModel m = (DefaultTableModel) tblBill.getModel();
            m.setRowCount(0);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
        
        txtGrandTotal.setText("0.0");
    }//GEN-LAST:event_btnOrderActionPerformed

