    private void btnLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadActionPerformed
        try {
            String q = "call displayitem()";
            String q2 = "call DisplayCategory()";
            
            rs = stmt.executeQuery(q);
            rs2 = stmt2.executeQuery(q2);
            rs.first();

            String itemname, price, category;

            itemname = rs.getString("item_name");
            price = rs.getString("rates");
            category = rs.getString("category_name");
            txtItemName.setText(itemname);
            txtPrice.setText(price);
            txtCategory.setText(category);
            
            
            // setting up the combobox values
            
            
            while(rs2.next())
            {   
                cmbCategory.addItem(rs2.getString("category_name"));
            }
                    
               

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
    }//GEN-LAST:event_btnLoadActionPerformed

