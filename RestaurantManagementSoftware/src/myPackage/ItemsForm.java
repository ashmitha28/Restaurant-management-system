    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            String name = txtItemName.getText();
            String cat = txtCategory.getText();
            String price = txtPrice.getText();

            String q = "call modifyitemrate('"+name+"', "+price+")";
            stmt.executeUpdate(q);
            
            JOptionPane.showMessageDialog(null, "Successfully updated the rate!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            int i = JOptionPane.showConfirmDialog(null, "sure");
            if (i == 0) {
                String item = txtItemName.getText();
                String cat = txtCategory.getText();
                String rate = txtPrice.getText();
                String q = "call manageitems('" + item+"', "+rate+", '"+cat+"', 'remove')";
                stmt.executeUpdate(q);
                JOptionPane.showMessageDialog(null, "Successfully deleted the item kindly reload!");

            } else {
                JOptionPane.showMessageDialog(null, "cancelled!!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

